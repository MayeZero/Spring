package com.example.spring.model;

public class Department {
    int Deptno;
    String Dname;

    public void setDeptno(int deptno) {
        Deptno = deptno;
    }

    public int getDeptno() {
        return Deptno;
    }

    public void setDname(String dname) {
        Dname = dname;
    }

    public String getDname() {
        return Dname;
    }
}
